#include "3-calc.h"
/**
 * op_add - Function that adds
 * @a: Number 1
 * @b: Number 2
 * Return: Addition of two numbers
 */
int op_add(int a, int b)
{
	return (a + b);
}
/**
 * op_sub - Function that adds
 * @a: Number 1
 * @b: Number 2
 * Return: Difference of two numbers
 */
int op_sub(int a, int b)
{
	return (a - b);
}
/**
 * op_mul - Function that adds
 * @a: Number 1
 * @b: Number 2
 * Return: Multiplication of two numbers
 */
int op_mul(int a, int b)
{
	return (a * b);
}
/**
 * op_div - Function that adds
 * @a: Number 1
 * @b: Number 2
 * Return: Dvision of two numbers
 */
int op_div(int a, int b)
{
	return (a / b);
}
/**
 * op_mod - Function that adds
 * @a: Number 1
 * @b: Number 2
 * Return: Modulo of two numbers
 */
int op_mod(int a, int b)
{
	return (a % b);
}
